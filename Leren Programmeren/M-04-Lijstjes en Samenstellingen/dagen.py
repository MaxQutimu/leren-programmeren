dagen = ["maandag","dinsdag","woensdag","donderdag","vrijdag","zaterdag","zondag"]

print(f"Dat zijn alle dagen in het week{dagen}")
print(f"Dit zijn alle werkdagen {dagen[0:5]}")
print(f"De weekenddagen zijn {dagen[5:7]}")
print(f"De alle dagen van de week in omgekeerde volgorde{dagen[::-1]}")
print(f"Dit zijn alle werk dagen in omgekeerd volgorde{dagen[4::-1]}")
print(f"Dit zijn weekenddage in omgekeerd volgorde{dagen[7:4:-1]}")